export function integrateTrapezoidalMethod(func, a, b, n) {
  const h = (b - a) / n;
  let sum = 0.5 * (func(a) + func(b));  

  for (let i = 1; i < n; i++) {
    const x = a + i * h;
    sum += func(x);
  }

  return sum * h;
}

