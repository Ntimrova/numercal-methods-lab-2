export function integrateMonteCarloMethod(func, a, b, n) {
  let sum = 0;

  for (let i = 0; i < n; i++) {
    const x = a + Math.random() * (b - a);  
    sum += func(x);
  }

  return ((b - a) / n) * sum;
}

